#{} est une concaténation. C'est un peu comme une sorte de lien entre les variables

#Le "puts" affiche procédé à l'écran avec un saut de ligne 
puts "On va compter le nombre d'heures de travail à THP"
#la ligne 6 fait une multiplication et utilisant une concaténation et le puts sert à afficher à l'écran
puts "Travail : #{10 * 5 * 11}"
#la ligne 8 est la même que la ligne 6
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"
#la ligne 10 affiche le string à l'écran
puts "Et en secondes ?"
#la ligne 12 fait la multiplication et affiche à l'écran la multiplication en utilisant "*"
puts 10 * 5 * 11 * 60 * 60
#la ligne 14 affiche un boolean 
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
#"+" fait l'addition des deux nombres et la signe "-" sert à soustraire les nombres àprès la signe de comparaison "<"
puts 3 + 2 < 5 - 7
#la ligne 18 et 19 affiche à l'écran la réponse de l'addition et la saoustraction tout en fesant l'addition et la soustraction
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
puts "Ça fait combien 5 - 7 ? #{5 - 7}"
#la ligne 21 affiche le string à l'écran
puts "Ok, c'est faux alors !"
#la ligne 22 affiche le string à l'écran
puts "C'est drôle ça, faisons-en plus :"
#la ligne 25, 26, 27 affiche aussi un boolean
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"
